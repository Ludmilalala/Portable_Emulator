class ПользовательскийИнтерфейс:
    """
    Класс для работы с пользовательским интерфейсом.

    Attributes:
        кнопки (dict): Словарь, хранящий состояние каждой кнопки.
    """

    def __init__():
        """
        Функция инициализации пользовательского интерфейса.
        """
        кнопки = {}

    def нарисовать_кнопку(название_кнопки):
        """
        Рисует кнопку на экране.

        Args:
            название_кнопки (str): Название кнопки для отображения.
        """
        вывести(f"Кнопка '{название_кнопки}' нарисована на экране.")

    def нажать_кнопку(название_кнопки):
        """
        Нажимает указанную кнопку.

        Args:
            название_кнопки (str): Название кнопки, которую нужно нажать.
        """
        кнопки[название_кнопки] = Истина
        вывести(f"Кнопка '{название_кнопки}' нажата.")

    def отпустить_кнопку(название_кнопки):
        """
        Отпускает указанную кнопку.

        Args:
            название_кнопки (str): Название кнопки, которую нужно отпустить.
        """
        кнопки[название_кнопки] = Ложь
        вывести(f"Кнопка '{название_кнопки}' отпущена.")
